package javaMock;

public class arrayStudy2 {

	public static void main(String[] args) 
	{
		
		// single dimensional - without using new keyword
		char division[]={'A','B','C'};     // capacity=length=3
		System.out.println(division);
		System.out.println(division.length);
		
		System.out.println(division[0]);
		
		
		for(int i=0;i<=division.length-1;i++)
		{
			System.out.println(division[i]);
		}
		
		
		

	}

}
